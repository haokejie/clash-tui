use std::{
    net::{IpAddr, Ipv4Addr, SocketAddr},
    path::Path,
    str::FromStr as _,
};

use anyhow::Result;
use serde::{Deserialize, Serialize};
use serde_yaml_ng::{Mapping, Value};

use crate::{
    constants::{network, tun},
    yaml,
};

#[derive(Default, Debug, Clone, PartialEq)]
pub struct IClashTemp(pub Mapping);

impl IClashTemp {
    #[must_use]
    pub fn template() -> Self {
        Self::template_with_ipc::<&Path>(None)
    }

    #[must_use]
    pub fn template_with_ipc<P: AsRef<Path>>(ipc_path: Option<P>) -> Self {
        let mut map = Mapping::new();
        let mut tun_config = Mapping::new();
        let ipc_path = ipc_path
            .as_ref()
            .and_then(|path| path.as_ref().to_str())
            .filter(|path| !path.is_empty());

        tun_config.insert("enable".into(), false.into());
        tun_config.insert("stack".into(), tun::DEFAULT_STACK.into());
        tun_config.insert("auto-route".into(), true.into());
        tun_config.insert("strict-route".into(), false.into());
        tun_config.insert("auto-detect-interface".into(), true.into());
        tun_config.insert(
            "dns-hijack".into(),
            Value::Sequence(tun::DNS_HIJACK.iter().map(|value| Value::from(*value)).collect()),
        );

        #[cfg(not(target_os = "windows"))]
        map.insert("redir-port".into(), network::ports::DEFAULT_REDIR.into());
        #[cfg(target_os = "linux")]
        map.insert("tproxy-port".into(), network::ports::DEFAULT_TPROXY.into());

        map.insert("tun".into(), Value::Mapping(tun_config));
        map.insert("mixed-port".into(), network::ports::DEFAULT_MIXED.into());
        map.insert("socks-port".into(), network::ports::DEFAULT_SOCKS.into());
        map.insert("port".into(), network::ports::DEFAULT_HTTP.into());
        map.insert("log-level".into(), "info".into());
        map.insert("allow-lan".into(), false.into());
        map.insert("ipv6".into(), true.into());
        map.insert("mode".into(), "rule".into());
        #[cfg(unix)]
        let uses_ipc_controller = ipc_path.is_some();
        #[cfg(not(unix))]
        let uses_ipc_controller = false;

        if !uses_ipc_controller {
            map.insert(
                "external-controller".into(),
                network::DEFAULT_EXTERNAL_CONTROLLER.into(),
            );
        }

        if let Some(ipc_path) = ipc_path {
            #[cfg(unix)]
            map.insert("external-controller-unix".into(), ipc_path.into());
            #[cfg(windows)]
            map.insert("external-controller-pipe".into(), ipc_path.into());
        }
        map.insert("secret".into(), "set-your-secret".into());
        map.insert("unified-delay".into(), true.into());

        Self(map)
    }

    pub async fn load_or_template(path: impl AsRef<Path>, ipc_path: Option<&Path>) -> Self {
        match yaml::read_mapping(path).await {
            Ok(map) => Self::from_mapping(map, ipc_path),
            Err(_) => Self::template_with_ipc(ipc_path),
        }
    }

    #[must_use]
    pub fn from_mapping(mut map: Mapping, ipc_path: Option<&Path>) -> Self {
        for (key, value) in Self::template_with_ipc(ipc_path).0 {
            map.entry(key).or_insert(value);
        }
        #[cfg(unix)]
        if ipc_path.is_some() {
            map.remove("external-controller");
            map.remove("external-controller-cors");
        }
        if let Some(Value::String(secret)) = map.get_mut("secret")
            && secret.is_empty()
        {
            *secret = "set-your-secret".into();
        }
        Self(Self::guard(map))
    }

    #[must_use]
    pub fn guard(mut config: Mapping) -> Mapping {
        #[cfg(not(target_os = "windows"))]
        config.insert(
            "redir-port".into(),
            guard_u16(&config, "redir-port", network::ports::DEFAULT_REDIR).into(),
        );
        #[cfg(target_os = "linux")]
        config.insert(
            "tproxy-port".into(),
            guard_u16(&config, "tproxy-port", network::ports::DEFAULT_TPROXY).into(),
        );

        config.insert("mixed-port".into(), Self::guard_mixed_port(&config).into());
        config.insert("socks-port".into(), Self::guard_socks_port(&config).into());
        config.insert("port".into(), Self::guard_port(&config).into());
        if config.contains_key("external-controller") {
            config.insert("external-controller".into(), Self::guard_server_ctrl(&config).into());
        }
        config
    }

    pub fn patch_config(&mut self, patch: &Mapping) {
        for (key, value) in patch {
            self.0.insert(key.clone(), value.clone());
        }
        self.0 = Self::guard(std::mem::take(&mut self.0));
    }

    pub async fn save_config(&self, path: impl AsRef<Path>) -> Result<()> {
        yaml::save_yaml(path, &self.0, Some("# Generated by Clash TUI")).await
    }

    #[must_use]
    pub fn get_client_info(&self) -> ClashInfo {
        let config = &self.0;

        ClashInfo {
            mixed_port: Self::guard_mixed_port(config),
            socks_port: Self::guard_socks_port(config),
            port: Self::guard_port(config),
            server: Self::guard_client_ctrl(config),
            secret: config.get("secret").and_then(scalar_to_string),
        }
    }

    #[must_use]
    pub fn guard_mixed_port(config: &Mapping) -> u16 {
        guard_u16(config, "mixed-port", network::ports::DEFAULT_MIXED)
    }

    #[must_use]
    pub fn guard_socks_port(config: &Mapping) -> u16 {
        guard_u16(config, "socks-port", network::ports::DEFAULT_SOCKS)
    }

    #[must_use]
    pub fn guard_port(config: &Mapping) -> u16 {
        guard_u16(config, "port", network::ports::DEFAULT_HTTP)
    }

    #[must_use]
    pub fn guard_server_ctrl(config: &Mapping) -> String {
        config
            .get("external-controller")
            .and_then(Value::as_str)
            .and_then(|value| {
                let value = value.trim();
                let value = if value.starts_with(':') {
                    format!("127.0.0.1{value}")
                } else {
                    value.to_owned()
                };
                SocketAddr::from_str(&value).ok().map(|socket| socket.to_string())
            })
            .unwrap_or_else(|| network::DEFAULT_EXTERNAL_CONTROLLER.into())
    }

    #[must_use]
    pub fn guard_client_ctrl(config: &Mapping) -> String {
        let value = Self::guard_server_ctrl(config);
        match SocketAddr::from_str(&value) {
            Ok(mut socket) => {
                if socket.ip().is_unspecified() {
                    socket.set_ip(IpAddr::V4(Ipv4Addr::new(127, 0, 0, 1)));
                }
                socket.to_string()
            }
            Err(_) => network::DEFAULT_EXTERNAL_CONTROLLER.into(),
        }
    }
}

fn guard_u16(config: &Mapping, key: &str, default: u16) -> u16 {
    let port = config.get(key).and_then(value_to_u16).unwrap_or(default);
    if port == 0 { default } else { port }
}

fn value_to_u16(value: &Value) -> Option<u16> {
    match value {
        Value::String(value) => value.parse().ok(),
        Value::Number(value) => value.as_u64().and_then(|value| u16::try_from(value).ok()),
        _ => None,
    }
}

fn scalar_to_string(value: &Value) -> Option<String> {
    match value {
        Value::String(value) => Some(value.clone()),
        Value::Bool(value) => Some(value.to_string()),
        Value::Number(value) => Some(value.to_string()),
        _ => None,
    }
}


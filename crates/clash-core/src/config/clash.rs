#[cfg(test)]
mod tests {
    use std::path::Path;

    use serde_yaml_ng::{Mapping, Value};

    use crate::constants::tun;

    use super::{ClashInfo, IClashTemp};

    #[test]
    fn default_template_has_stable_client_info() {
        let info = IClashTemp::template().get_client_info();

        assert_eq!(
            info,
            ClashInfo {
                mixed_port: 7897,
                socks_port: 7898,
                port: 7899,
                server: "127.0.0.1:9097".into(),
                secret: Some("set-your-secret".into()),
            }
        );
    }

    #[test]
    fn default_template_includes_tun_defaults() -> Result<(), &'static str> {
        let template = IClashTemp::template();
        let tun_config = template
            .0
            .get("tun")
            .and_then(Value::as_mapping)
            .ok_or("tun defaults")?;

        assert_eq!(tun_config.get("enable"), Some(&Value::from(false)));
        assert_eq!(tun_config.get("stack"), Some(&Value::from(tun::DEFAULT_STACK)));
        assert_eq!(tun_config.get("auto-route"), Some(&Value::from(true)));
        assert_eq!(tun_config.get("strict-route"), Some(&Value::from(false)));
        assert_eq!(tun_config.get("auto-detect-interface"), Some(&Value::from(true)));
        assert_eq!(
            tun_config.get("dns-hijack").and_then(Value::as_sequence).map(Vec::len),
            Some(tun::DNS_HIJACK.len())
        );
        Ok(())
    }

    #[test]
    #[cfg(unix)]
    fn ipc_template_does_not_expose_tcp_external_controller() {
        let template = IClashTemp::template_with_ipc(Some(Path::new("/tmp/app_settings.sock")));

        assert!(template.0.get("external-controller").is_none());
        assert!(template.0.get("external-controller-cors").is_none());
        assert_eq!(
            template.0.get("external-controller-unix"),
            Some(&Value::from("/tmp/app_settings.sock"))
        );
    }

    #[test]
    #[cfg(unix)]
    fn ipc_mapping_strips_stale_external_controller_fields() {
        let mut map = Mapping::new();
        map.insert("external-controller".into(), Value::from("0.0.0.0:8080"));
        map.insert("external-controller-cors".into(), Value::Mapping(Mapping::new()));

        let config = IClashTemp::from_mapping(map, Some(Path::new("/tmp/app_settings.sock")));

        assert!(config.0.get("external-controller").is_none());
        assert!(config.0.get("external-controller-cors").is_none());
        assert_eq!(
            config.0.get("external-controller-unix"),
            Some(&Value::from("/tmp/app_settings.sock"))
        );
    }

    #[test]
    fn invalid_ports_fall_back_instead_of_wrapping() {
        let mut map = Mapping::new();
        map.insert("mixed-port".into(), Value::from(65537_u64));
        map.insert("socks-port".into(), Value::from(0_u64));
        map.insert("port".into(), Value::from("abc"));
        map.insert("external-controller".into(), Value::from("0.0.0.0:8080"));

        let info = IClashTemp(IClashTemp::guard(map)).get_client_info();

        assert_eq!(info.mixed_port, 7897);
        assert_eq!(info.socks_port, 7898);
        assert_eq!(info.port, 7899);
        assert_eq!(info.server, "127.0.0.1:8080");
    }
}
